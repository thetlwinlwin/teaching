# integer
x = 78
x_type = type(x)
print(x_type)


# real -> float
x = -7.08
x_type = type(x)
print(x_type)

# char (no concept in python)
x = "B"
x_type = type(x)
print(x_type)


# str -> str
x = "this is string"
x_type = type(x)
print(x_type)

# boolean -> bool
x = True
x_type = type(x)
print(x_type)
