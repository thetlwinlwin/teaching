# True False (comparison)

# greater <, greater than or equal  <=
# less >, less than or equal >=
alphabet_a = 'a'
alphabet_b = 'b'

print(alphabet_a < alphabet_b)  # True <alphabet ascending>
name1 = 'Naurto'
name2 = 'Sasuke'
print(name1 < name2)  # True

# S big vs S small
print('Z' < 'a')  # True lowercase letter wins

print('Z' < 'Z')  # False
